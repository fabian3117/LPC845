void I4017::SetDigito( void )
{
	if ( m_indice )
		SetClock ();
	else
		SetReset();

	m_indice ++;
	m_indice %= m_maxsalidas;
}
void I4017::Inicializar ( void )
{
	for ( uint8_t i = 0 ; i < m_pinesI4017.size() ; i++)
		m_pinesI4017[i]->SetDir();
}
void I4017::SetClock (void)
{
	m_pinesI4017[0]->ClrPin();

	m_pinesI4017[1]->ClrPin();
	m_pinesI4017[1]->SetPin();
	m_pinesI4017[1]->ClrPin();
}

void I4017::SetReset(void)
{
	m_pinesI4017[0]->ClrPin();
	m_pinesI4017[0]->SetPin();
	m_pinesI4017[0]->ClrPin();
}
